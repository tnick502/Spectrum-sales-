export function formatCarrierInfo(info: any): { label: string; value: string }[] {
  const formattedInfo: { label: string; value: string }[] = [];

  function processValue(key: string, value: any) {
    if (typeof value === 'string') {
      formattedInfo.push({ label: key, value });
    } else if (typeof value === 'object' && value !== null) {
      Object.entries(value).forEach(([subKey, subValue]) => {
        if (typeof subValue === 'string') {
          formattedInfo.push({
            label: `${key} (${subKey})`,
            value: subValue
          });
        } else {
          processValue(`${key} ${subKey}`, subValue);
        }
      });
    }
  }

  Object.entries(info).forEach(([key, value]) => {
    processValue(key, value);
  });

  return formattedInfo;
}